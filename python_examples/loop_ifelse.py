def max_zero_occurrence(number_list):
    max_ocurrencie = 0
    current_ocurrencie = 0

    for number in number_list:  # number_list
        if number == 0:
            current_ocurrencie += 1
            if current_ocurrencie > max_ocurrencie:
                max_ocurrencie = current_ocurrencie
        else:
            current_ocurrencie = 0

    return max_ocurrencie

print( max_zero_occurrence([2,0,0,0,1,0,3]) )

